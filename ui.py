def draw_bee(surface, x, y, is_best=False, is_hit=False, is_stung=False):
    """Отрисовка пчелы."""
    radius = config.BEE_RADIUS_BEST if is_best else config.BEE_RADIUS
    if is_stung:
        body_color = config.COLOR_BEE_STUNG
    elif is_hit:
        body_color = config.COLOR_BEE_HIT
    elif is_best:
        body_color = config.COLOR_BEE_BEST
    else:
        body_color = config.COLOR_BEE_YELLOW

    pygame.draw.circle(surface, config.COLOR_BEE_BLACK, (int(x), int(y)), radius + 1)
    pygame.draw.circle(surface, body_color, (int(x), int(y)), radius)

    stripe_offset = radius // 3
    for s_y in [-stripe_offset, stripe_offset]:
        pygame.draw.rect(surface, config.COLOR_BEE_BLACK,
                         (int(x) - radius + 2, int(y + s_y - config.STRIPE_WIDTH // 2), radius * 2 - 4, config.STRIPE_WIDTH))


