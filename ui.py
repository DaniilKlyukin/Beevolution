        "  Клавиша [C]: Удалить все стены",
        "  Клавиша [D]: Стереть последнюю",
        "  Клавиша [R]: Сброс всей эволюции",
        "  [SPACE]: +10 | [F]: +50 | [G]: +200",
        "  Выход: клавиша [ESC]",
