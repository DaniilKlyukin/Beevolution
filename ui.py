    hud_height = config.SCREEN_SIZE[1] - 40

    ui_bg = pygame.Surface((hud_width, hud_height))
    ui_bg.fill((10, 10, 20))
    ui_bg.set_alpha(140)  # Снижена непрозрачность с 220 до 140
